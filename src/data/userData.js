// Existing showMessage function preserved
export function showMessage(message, type = 'info', timeout = 3000) {
  // Remove existing messages
  const existingMessages = document.querySelectorAll('.floating-message');
  existingMessages.forEach(msg => msg.remove());
  
  // Create new message element
  const messageDiv = document.createElement('div');
  messageDiv.className = `floating-message alert alert-${type}`;
  messageDiv.style.cssText = `
    position: fixed;
    top: 20px;
    right: 20px;
    z-index: 9999;
    min-width: 300px;
    opacity: 0;
    transform: translateX(100%);
    transition: all 0.3s ease;
  `;
  messageDiv.textContent = message;
  
  // Add to document
  document.body.appendChild(messageDiv);
  
  // Animate in
  setTimeout(() => {
    messageDiv.style.opacity = '1';
    messageDiv.style.transform = 'translateX(0)';
  }, 10);
  
  // Auto remove
  if (timeout > 0) {
    setTimeout(() => {
      messageDiv.style.opacity = '0';
      messageDiv.style.transform = 'translateX(100%)';
      setTimeout(() => {
        const element = document.querySelector('.floating-message');
        if (element) {
          element.remove();
        }
      }, timeout);
    });
  }
}